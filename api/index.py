def get_rich_sermon():
    """ በየ 30 ደቂቃው የተለያየና ጥልቅ ትምህርት የሚመርጥ ቀመር """
    now = datetime.utcnow() + timedelta(hours=3) # የኢትዮጵያ ሰዓት
    slot_index = (now.hour * 2) + (1 if now.minute >= 30 else 0)
    sermon_idx = slot_index % len(DEEP_SERMONS_POOL)
    return DEEP_SERMONS_POOL[sermon_idx]

